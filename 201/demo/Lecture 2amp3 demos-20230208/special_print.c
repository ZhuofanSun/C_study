#include <stdio.h>

int main (void){
    printf("This is what an alert looks like:\aAfter alert\n");
    printf("This is what a backspace looks like:\bAfter Backspace\n");
    printf("This is a newline:\n\n");
    printf("This is a tab:\tAfter tab\n");
    printf("This is a quote:\"\n");
    printf("This is a backslash:\\\n");
    printf("This is a percentage:%%\n");

    return 0;
}
